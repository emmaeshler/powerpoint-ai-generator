function renderCalloutBar(s: any, comp: any, r: LayoutRect, t: DeckTheme) {
  if (!comp.text) return;

  let text = comp.text;
  let fontSize = 14;

  // Dynamic font sizing based on length
  if (text.length > 140) {
    fontSize = 11;
    // Truncate with ellipsis if still too long
    if (text.length > 180) {
      text = text.substring(0, 177) + '...';
    }
  } else if (text.length > 100) {
    fontSize = 12;
  }

  // Orange accent stripe on top — draws the eye to the callout as a "landing zone"
  const accentH = 0.035;
  s.addShape('rect', { x: r.x, y: r.y, w: r.w, h: accentH, fill: { color: t.colors.accent2 }, line: { type: 'none' } });

  // Main callout background — navy, slightly inset below accent
  const barY = r.y + accentH;
  const barH = r.h - accentH;
  s.addShape('rect', { x: r.x, y: barY, w: r.w, h: barH, fill: { color: t.colors.dk1 }, line: { type: 'none' } });

  // Left accent bar — vertical orange stripe for visual anchoring
  const leftBarW = 0.06;
  s.addShape('rect', { x: r.x, y: barY, w: leftBarW, h: barH, fill: { color: t.colors.accent2 }, line: { type: 'none' } });

  // Callout text — slightly larger, with more left padding to clear accent bar
  s.addText(text, {
    x: r.x + 0.35,
    y: barY,
    w: r.w - 0.55,
    h: barH,
    fontSize,
    bold: true,
    color: t.colors.lt1,
    fontFace: 'Calibri',
    align: 'left',
    valign: 'middle',
    wrap: true
  });
}

