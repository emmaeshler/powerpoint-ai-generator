import pptxgen from 'pptxgenjs';
import { Slide, DeckTheme, BrandColor, getSlideComponents } from '../types';
import { DECK_THEME } from '../constants';
import { compose, computeCardLayout, LayoutRect, BODY_W, BODY_LEFT, CARD_GUTTER, MAX_CARD_W, CALLOUT_Y } from './composition-engine';

export function generatePowerPoint(slides: Slide[], theme: DeckTheme = DECK_THEME): void {
  const pptx = new pptxgen();

  pptx.layout = 'LAYOUT_WIDE';
  pptx.author = 'Emma\'s Awesome PPT Generator';
  pptx.title = 'INSIGHT2PROFIT Presentation';
  pptx.subject = 'Generated by Emma\'s Awesome PPT Generator';

  pptx.defineLayout({ name: 'EXEC_WIDE', width: theme.slideSize.width, height: theme.slideSize.height });
  pptx.layout = 'EXEC_WIDE';

  slides.forEach((slide) => renderSlide(pptx, slide, theme));

  pptx.writeFile({ fileName: 'INSIGHT2PROFIT_Presentation.pptx' });
}

// ─── SLIDE RENDERER ───────────────────────────────────────────

function renderSlide(pptx: pptxgen, slide: Slide, t: DeckTheme) {
  const s = pptx.addSlide();
  s.background = { color: t.colors.lt1 };

  // Check if this is a special direction_proof slide type
  const slideData = slide as any;
  if (slideData.type === 'direction_proof') {
    renderDirectionProofSlide(s, slideData, t);
    addFooter(s, t);
    return;
  }

  const contentStartY = addTitleWithDivider(s, slide, t);

  // Composition engine: template-driven grid layout
  const layout = compose(slide.templateId, slide.slotContent, slide.calloutBar);

  // Render each component at its computed position
  const allComponents = getSlideComponents(slide);
  for (const comp of allComponents) {
    const region = layout.get(comp.id);
    if (!region) continue;

    switch (comp.type) {
      case 'bullet_list': renderBulletList(s, comp, region, t); break;
      case 'stat_hero': renderStatHero(s, comp, region, t); break;
      case 'kpi_cards': renderKpiCards(s, comp, region, t); break;
      case 'icon_columns': renderIconColumns(s, comp, region, t); break;
      case 'process_flow': renderProcessFlow(s, comp, region, t); break;
      case 'data_table': renderDataTable(s, comp, region, t); break;
      case 'simple_table': renderSimpleTable(s, comp, region, t); break;
      case 'chart': renderChart(pptx, s, comp, region, t); break;
      case 'callout_bar': renderCalloutBar(s, comp, region, t); break;
      case 'timeline_track': renderTimelineTrack(s, comp, region, t); break;
      case 'icon_grid': renderIconGrid(s, comp, region, t); break;
      case 'screenshot': renderScreenshot(s, comp, region, t); break;
      case 'text_block': renderTextBlock(s, comp, region, t); break;
      case 'comparison_bars': renderComparisonBars(s, comp, region, t); break;
      case 'team_structure': renderTeamStructure(s, comp, region, t); break;
      case 'waterfall_chart': renderWaterfallChart(s, comp, region, t); break;
      case 'context_banner': renderContextBanner(s, comp, region, t); break;
      default:
        // Fallback for unrecognized component types
        console.warn(`Unknown component type: ${comp.type} — rendering as bullet_list fallback`);
        renderBulletList(s, comp, region, t);
        break;
    }
  }

  addFooter(s, t);
}

// ─── COMMON HELPERS ───────────────────────────────────────────

function addFooter(s: any, t: DeckTheme) {
  s.addShape('rect', { x: 0.75, y: 6.85, w: 11.833, h: 0.015, fill: { color: t.colors.accent1 }, line: { type: 'none' } });
  s.addText('INSIGHT2PROFIT', { x: 0.75, y: 6.95, w: 2.0, h: 0.3, fontSize: 10, color: t.colors.dk1, fontFace: 'Calibri', align: 'left', valign: 'middle', bold: true });
  s.addText('1', { x: 11.0, y: 6.95, w: 1.583, h: 0.3, fontSize: 10, color: t.colors.accent1, fontFace: 'Calibri', align: 'right', valign: 'middle' });
}

function addTitleWithDivider(s: any, slide: Slide, t: DeckTheme, y: number = 0.6) {
  const title = slide.title;
  const parts = title.split('|').map(p => p.trim());
  const boldPart = parts[0] || title;
  const regularPart = parts[1] || '';

  // Dynamic font size based on title length to prevent overflow
  const totalLength = title.length;
  let fontSize = 24;
  if (totalLength > 80) {
    fontSize = 16;
  } else if (totalLength > 60) {
    fontSize = 18;
  }

  let currentY = y;

  // Render title
  if (regularPart) {
    s.addText([
      { text: boldPart, options: { bold: true, color: t.colors.dk1, fontSize } },
      { text: ' | ', options: { color: t.colors.dk1, fontSize } },
      { text: regularPart, options: { color: t.colors.dk1, fontSize } }
    ], {
      x: 0.75,
      y: currentY,
      w: 11.833,
      h: 0.45,
      fontFace: 'Calibri',
      align: 'left',
      valign: 'middle'
    });
  } else {
    s.addText(boldPart, {
      x: 0.75,
      y: currentY,
      w: 11.833,
      h: 0.45,
      fontSize,
      bold: true,
      color: t.colors.dk1,
      fontFace: 'Calibri',
      align: 'left',
      valign: 'middle'
    });
  }

  currentY += 0.5;

  // Render key metric
  if (slide.keyMetric) {
    s.addText(slide.keyMetric, {
      x: 0.75,
      y: currentY,
      w: 11.833,
      h: 0.5,
      fontSize: 36,
      bold: true,
      color: t.colors.accent3,  // Teal
      fontFace: 'Calibri',
      align: 'center',
      valign: 'middle'
    });

    currentY += 0.55;

    if (slide.keyMetricLabel) {
      s.addText(slide.keyMetricLabel, {
        x: 0.75,
        y: currentY,
        w: 11.833,
        h: 0.3,
        fontSize: 14,
        color: t.colors.accent1,  // Gray
        fontFace: 'Calibri',
        align: 'center',
        valign: 'top'
      });

      currentY += 0.35;
    }
  }

  // Divider line
  s.addShape('rect', {
    x: 0.75,
    y: currentY,
    w: 11.833,
    h: 0.02,
    fill: { color: t.colors.accent1 },
    line: { type: 'none' }
  });

  // Return the Y position after title area for content positioning
  return currentY + 0.05;
}

