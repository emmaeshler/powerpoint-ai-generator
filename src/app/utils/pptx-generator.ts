function renderWaterfallChart(s: any, comp: any, r: LayoutRect, t: DeckTheme) {
  const bars = comp.bars || [];
  if (bars.length === 0) return;

  // Calculate cumulative values
  let cumulative = 0;
  const processedBars = bars.map((bar: any) => {
    const isFirst = bar.value > 0 && !bar.delta;
    const isTotal = bar.isTotal;

    if (isFirst) {
      cumulative = bar.value;
      return { ...bar, startValue: 0, endValue: bar.value, cumulative };
    } else if (isTotal) {
      return { ...bar, startValue: 0, endValue: bar.value, cumulative: bar.value };
    } else {
      const delta = bar.delta || 0;
      const startValue = cumulative;
      cumulative += delta;
      return { ...bar, startValue, endValue: cumulative, cumulative, delta };
    }
  });

  // Find max value for scaling
  const allValues = processedBars.flatMap((b: any) => [b.startValue, b.endValue]);
  const maxValue = Math.max(...allValues, 0);
  const minValue = Math.min(...allValues, 0);
  const range = maxValue - minValue;

  // Chart dimensions
  const chartH = r.h - 0.8;  // Leave space for labels
  const chartW = r.w - 0.5;
  const chartX = r.x + 0.25;
  const chartY = r.y + 0.2;
  const baseY = chartY + chartH;

  const barW = (chartW / bars.length) * 0.7;
  const gap = (chartW / bars.length) * 0.3;

  // Draw bars
  processedBars.forEach((bar: any, i: number) => {
    const x = chartX + i * (barW + gap);
    const isFirst = i === 0;
    const isTotal = bar.isTotal;
    const delta = bar.delta || 0;

    // Calculate bar position and height
    const startY = baseY - (bar.startValue / maxValue) * chartH;
    const endY = baseY - (bar.endValue / maxValue) * chartH;
    const barHeight = Math.abs(endY - startY);
    const barTop = Math.min(startY, endY);

    // Bar color
    const isPositive = delta > 0;
    const barColor = isTotal || isFirst ? t.colors.dk1 : isPositive ? t.colors.accent3 : t.colors.accent5;

    // Draw bar
    s.addShape('rect', {
      x, y: barTop, w: barW, h: Math.max(barHeight, 0.05),
      fill: { color: barColor }, line: { color: t.colors.dk1, width: 0.5 }
    });

    // Connector line to next bar
    if (comp.showConnectors && i < processedBars.length - 1) {
      s.addShape('line', {
        x, y: endY, w: barW + gap, h: 0,
        line: { color: t.colors.accent1, width: 1, dashType: 'dash' }
      });
    }

    // Value label on top of bar
    const valueText = isFirst || isTotal
      ? `$${bar.endValue}M`
      : (delta > 0 ? `+$${delta}M` : `$${delta}M`);

    s.addText(valueText, {
      x, y: barTop - 0.25, w: barW, h: 0.2,
      fontSize: 9, bold: true, color: t.colors.dk1,
      fontFace: 'Calibri', align: 'center', valign: 'middle'
    });

    // Bar label at bottom
    s.addText(bar.label, {
      x, y: baseY + 0.05, w: barW, h: 0.3,
      fontSize: 8, color: t.colors.dk1, fontFace: 'Calibri',
      align: 'center', valign: 'top', breakLine: true
    });

    // Delta label inside bar if provided and space available
    if (bar.deltaLabel && barHeight > 0.3) {
      s.addText(bar.deltaLabel, {
        x, y: barTop + barHeight / 2 - 0.1, w: barW, h: 0.2,
        fontSize: 7, bold: true, color: 'FFFFFF',
        fontFace: 'Calibri', align: 'center', valign: 'middle'
      });
    }
  });

  // Render annotations
  const annotations = comp.annotations || [];
  annotations.forEach((ann: any) => {
    const targetIndex = ann.targetBar;
    if (targetIndex >= processedBars.length) return;

    const bar = processedBars[targetIndex];
    const x = chartX + targetIndex * (barW + gap);
    const barHeight = Math.abs(baseY - (bar.endValue / maxValue) * chartH - (baseY - (bar.startValue / maxValue) * chartH));
    const barTop = baseY - Math.max(bar.startValue, bar.endValue) / maxValue * chartH;
    const annColor = ann.color === 'orange' ? t.colors.accent2 : t.colors.accent3;

    if (ann.side === 'above') {
      // Connector line
      s.addShape('rect', {
        x: x + barW / 2 - 0.01, y: barTop - 0.35, w: 0.02, h: 0.3,
        fill: { color: annColor }, line: { type: 'none' }
      });

      // Annotation box
      s.addShape('rect', {
        x: x - 0.3, y: barTop - 0.65, w: barW + 0.6, h: 0.28,
        fill: { color: annColor }, line: { type: 'none' }
      });

      s.addText(ann.text, {
        x: x - 0.25, y: barTop - 0.65, w: barW + 0.5, h: 0.28,
        fontSize: 8, bold: true, color: 'FFFFFF', fontFace: 'Calibri',
        align: 'center', valign: 'middle'
      });
    }
  });
}

function renderTeamStructure(s: any, comp: any, r: LayoutRect, t: DeckTheme) {
  const teams = comp.teams || [];
  if (teams.length === 0) return;

  // Divider line above team section
  s.addShape('rect', {
    x: r.x, y: r.y, w: r.w, h: 0.015,
    fill: { color: t.colors.accent1 }, line: { type: 'none' }
  });

  const teamAreaY = r.y + 0.1;
  const teamAreaH = r.h - 0.1;

  // Background
  s.addShape('rect', {
    x: r.x, y: teamAreaY, w: r.w, h: teamAreaH,
    fill: { color: 'F8FAFB' }, line: { type: 'none' }
  });

  // Connector symbol between teams (e.g. "+")
  const connector = comp.connector || '+';

  // Calculate layout: distribute teams evenly with space for connectors
  const teamW = (r.w - 1.0) / teams.length;
  const connectorW = teams.length > 1 ? 1.0 / (teams.length - 1) : 0;

  teams.forEach((team: any, i: number) => {
    const x = r.x + i * (teamW + (i > 0 ? connectorW : 0));

    // Person icon circle
    s.addShape('ellipse', {
      x: x + teamW / 2 - 0.4,
      y: teamAreaY + 0.15,
      w: 0.8, h: 0.8,
      fill: { color: t.colors.dk1 }
    });
    const iconText = team.icon || '\u{1F464}';
    s.addText(iconText, {
      x: x + teamW / 2 - 0.4,
      y: teamAreaY + 0.15,
      w: 0.8, h: 0.8,
      fontSize: 20, color: t.colors.lt1,
      fontFace: 'Calibri', align: 'center', valign: 'middle'
    });

    // Team name
    s.addText(team.name, {
      x, y: teamAreaY + 1.05, w: teamW, h: 0.35,
      fontSize: 13, bold: true, color: t.colors.dk1,
      fontFace: 'Calibri', align: 'center', valign: 'middle'
    });

    // Sections (heading + bullets) - positioned to the right of icon
    let sectionY = teamAreaY + 0.15;
    const sectionX = x + teamW / 2 + 0.5;
    const sectionW = teamW / 2 - 0.6;

    (team.sections || []).forEach((section: any) => {
      s.addText(section.heading, {
        x: sectionX, y: sectionY, w: sectionW, h: 0.25,
        fontSize: 11, bold: true, color: t.colors.dk1,
        fontFace: 'Calibri', align: 'left', valign: 'middle'
      });
      sectionY += 0.28;

      const bullets = (section.items || []).map((b: string) => ({ text: b, options: {} }));
      const bulletH = bullets.length * 0.22;
      if (bullets.length > 0) {
        s.addText(bullets, {
          x: sectionX, y: sectionY, w: sectionW, h: bulletH,
          fontSize: 10, color: t.colors.dk1, fontFace: 'Calibri',
          bullet: { type: 'bullet', characterCode: '2022',
            color: t.colors.accent1 },
          lineSpacing: 12, valign: 'top', italic: true
        });
        sectionY += bulletH + 0.15;
      }
    });

    // Connector "+" between teams
    if (i < teams.length - 1) {
      const connX = x + teamW + connectorW / 2 - 0.2;
      s.addText(connector, {
        x: connX, y: teamAreaY + teamAreaH / 2 - 0.25,
        w: 0.4, h: 0.5,
        fontSize: 24, bold: true, color: t.colors.accent1,
        fontFace: 'Calibri', align: 'center', valign: 'middle'
      });
    }
  });
}

