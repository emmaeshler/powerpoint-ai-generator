function renderSimpleTable(s: any, comp: any, r: LayoutRect, t: DeckTheme) {
  const headers = comp.headers || [];
  const rows = comp.rows || [];
  const title = comp.title;

  if (headers.length === 0 || rows.length === 0) return;

  const colCount = headers.length + 1; // +1 for the label column
  const colW = r.w / colCount;

  const titleH = title ? 0.25 : 0;
  const headerH = 0.35;
  const rowH = 0.35;

  // Smaller font for dense tables (4+ rows)
  const fontSize = rows.length >= 4 ? 10 : 11;

  let currentY = r.y;

  // Render title if present
  if (title) {
    s.addShape('rect', {
      x: r.x,
      y: currentY,
      w: r.w,
      h: titleH,
      fill: { color: t.colors.lt2 },
      line: { color: t.colors.accent1, width: 0.5 }
    });
    s.addText(title, {
      x: r.x + 0.1,
      y: currentY,
      w: r.w - 0.2,
      h: titleH,
      fontSize: 14,
      bold: true,
      color: t.colors.dk1,
      fontFace: 'Calibri',
      align: 'left',
      valign: 'middle'
    });
    currentY += titleH;
  }

  // Render header row
  // First header cell (empty - above row labels)
  s.addShape('rect', {
    x: r.x,
    y: currentY,
    w: colW,
    h: headerH,
    fill: { color: t.colors.lt2 },
    line: { color: t.colors.accent1, width: 1.5 }
  });

  // Column headers
  headers.forEach((header: string, i: number) => {
    const x = r.x + (i + 1) * colW;
    s.addShape('rect', {
      x,
      y: currentY,
      w: colW,
      h: headerH,
      fill: { color: t.colors.lt2 },
      line: { color: t.colors.accent1, width: 1.5 }
    });
    s.addText(header, {
      x,
      y: currentY,
      w: colW,
      h: headerH,
      fontSize: 12,
      bold: true,
      color: t.colors.dk1,
      fontFace: 'Calibri',
      align: 'center',
      valign: 'middle'
    });
  });

  currentY += headerH;

  // Render data rows
  rows.forEach((row: any, ri: number) => {
    const bgColor = ri % 2 === 0 ? t.colors.lt1 : t.colors.accent6;

    // Row label
    s.addShape('rect', {
      x: r.x,
      y: currentY,
      w: colW,
      h: rowH,
      fill: { color: bgColor },
      line: { color: t.colors.accent1, width: 0.5 }
    });
    s.addText(row.label || '', {
      x: r.x + 0.1,
      y: currentY,
      w: colW - 0.2,
      h: rowH,
      fontSize,
      bold: true,
      color: t.colors.dk1,
      fontFace: 'Calibri',
      align: 'left',
      valign: 'middle'
    });

    // Row values
    (row.values || []).forEach((val: string, vi: number) => {
      const x = r.x + (vi + 1) * colW;
      s.addShape('rect', {
        x,
        y: currentY,
        w: colW,
        h: rowH,
        fill: { color: bgColor },
        line: { color: t.colors.accent1, width: 0.5 }
      });
      s.addText(val || '', {
        x,
        y: currentY,
        w: colW,
        h: rowH,
        fontSize,
        color: t.colors.dk1,
        fontFace: 'Calibri',
        align: 'center',
        valign: 'middle'
      });
    });

    currentY += rowH;
  });
}

